import 'dart:async';

/// Утилита для debounce поисковых запросов.
///
/// Откладывает выполнение действия до тех пор, пока не пройдет
/// указанная задержка без новых вызовов.
class SearchDebouncer {
  /// Создает экземпляр SearchDebouncer с указанной задержкой.
  ///
  /// [delay] - время задержки перед выполнением действия.
  SearchDebouncer({required this.delay});

  /// Задержка перед выполнением действия.
  final Duration delay;

  Timer? _timer;

  /// Вызывает действие с debounce.
  ///
  /// Отменяет предыдущий таймер (если есть) и создает новый.
  /// Действие будет выполнено только если в течение [delay]
  /// не будет новых вызовов.
  ///
  /// [action] - действие для выполнения после задержки.
  void call(void Function() action) {
    _timer?.cancel();
    _timer = Timer(delay, action);
  }

  /// Освобождает ресурсы и отменяет ожидающий таймер.
  ///
  /// Должен быть вызван при уничтожении объекта,
  /// использующего debouncer (например, в dispose виджета).
  void dispose() {
    _timer?.cancel();
    _timer = null;
  }
}
